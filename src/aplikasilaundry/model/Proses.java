package aplikasilaundry.model;

//Model tabel proses
public class Proses {

    //Field
    private int idProses;
    private String namaProses;

    //Method mengambil ID proses
    public int getIdProses() {

        return idProses;

    }

    //Method mengisi ID proses
    public void setIdProses(int idProses) {

        this.idProses = idProses;

    }

    //Method mengambil nama proses
    public String getNamaProses() {

        return namaProses;

    }

    //Method mengisi nama proses
    public void setNamaProses(String namaProses) {

        this.namaProses = namaProses;

    }

}